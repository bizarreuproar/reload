int	ft_iterative_factorial(int nb)
{
	int	num;

	num = 1;
	if (nb >= 0 && nb <= 12)
	{
		while (nb > 0)
		{
			num *= nb;
			nb -= 1;
		}
		return (num);
	}
	else
		return (0);
}
